package com.rent.baseinfo.vo;

/**
 * 参数从表
 */
public class DictVo  {

    private String name;// 名称
    private String value;// 参数值

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}